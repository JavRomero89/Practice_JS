// Definimos una variable de tipo string
let myString = "Hola mundo";

// Definimos una variable de tipo number
let myNumber = 42;

// Definimos una variable de tipo boolean
let myBoolean = true;

// Definimos una variable de tipo null
let myNull = null;

// Definimos una variable de tipo undefined
let myUndefined;

// Definimos una variable de tipo object
let myObject = { name: "John", age: 30 };



// Comprobamos el tipo de datos de cada variable
console.log(typeof myString); // Output: string
console.log(typeof myNumber); // Output: number
console.log(typeof myBoolean); // Output: boolean
console.log(typeof myNull); // Output: object
console.log(typeof myUndefined); // Output: undefined
console.log(typeof myObject); // Output: object
